def search_rules(query):
    query = query.lower().strip()
    cursor.execute('''
    SELECT id, category, title, content FROM fbi_rules 
    WHERE title LIKE ? OR content LIKE ?
    LIMIT 10
    ''', (f'%{query}%', f'%{query}%'))
    return cursor.fetchall()

