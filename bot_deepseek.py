@bot.callback_query_handler(func=lambda call: True)
def callback_handler(call):
    rule_key = call.data
    
    if rule_key in rules_db:
        rule = rules_db[rule_key]
        response = f"**{rule['title']}**\n\n```\n{rule['text']}\n```"
        
        # Добавляем кнопку "Назад" в зависимости от раздела
        back_markup = types.InlineKeyboardMarkup()
        
        # Определяем, к какому разделу относится правило
        if rule_key.startswith("gov_"):
            back_markup.add(types.InlineKeyboardButton("◀ Назад к Правительству", callback_data="back_to_gov"))
        elif rule_key.startswith("smi_"):
            back_markup.add(types.InlineKeyboardButton("◀ Назад к СМИ", callback_data="back_to_smi"))
        elif rule_key.startswith("pro_"):
            back_markup.add(types.InlineKeyboardButton("◀ Назад к ПРО", callback_data="back_to_pro"))
        elif rule_key.startswith("ppe_"):
            back_markup.add(types.InlineKeyboardButton("◀ Назад к ППЭ", callback_data="back_to_ppe"))
        elif rule_key.startswith("ppa_"):
            back_markup.add(types.InlineKeyboardButton("◀ Назад к ППА", callback_data="back_to_ppa"))
        elif rule_key.startswith("ppg_"):
            back_markup.add(types.InlineKeyboardButton("◀ Назад к ППГ", callback_data="back_to_ppg"))
        
        bot.edit_message_text(
            response,
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            reply_markup=back_markup,
            parse_mode="Markdown"
        )
    
    elif call.data == "back_to_gov":
        gov_menu(call.message)
    elif call.data == "back_to_smi":
        smi_menu(call.message)
    elif call.data == "back_to_pro":
        pro_menu(call.message)
    elif call.data == "back_to_ppe":
        ppe_menu(call.message)
    elif call.data == "back_to_ppa":
        ppa_menu(call.message)
    elif call.data == "back_to_ppg":
        ppg_menu(call.message)

