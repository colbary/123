# ========== ПОИСК ==========
@bot.callback_query_handler(func=lambda call: call.data == "search")
def search_prompt(call):
    bot.edit_message_text("🔍 Введите ключевое слово для поиска:", call.message.chat.id, call.message.message_id, reply_markup=None)
    bot.register_next_step_handler(call.message, search_results)

def search_results(message):
    query = message.text.lower()
    results = []
    for key, text in TEXTS.items():
        if query in text.lower() or query in key.lower():
            results.append((key, TEXTS[key][:100] + "..."))
    
    if not results:
        bot.send_message(message.chat.id, "❌ Ничего не найдено. Попробуйте другое слово.")
        return
    
    markup = InlineKeyboardMarkup(row_width=1)
    for key, preview in results[:10]:
        markup.add(InlineKeyboardButton(preview[:50], callback_data=key))
    markup.add(InlineKeyboardButton("◀️ Главное меню", callback_data="main"))
    bot.send_message(message.chat.id, f"🔍 Найдено {len(results)} результатов:", reply_markup=markup)

# ========== ОБРАБОТКА КНОПОК ==========
@bot.callback_query_handler(func=lambda call: True)
def handle_callback(call):
    if call.data == "main":
        bot.edit_message_text("📚 **Главное меню**\nВыберите раздел:", call.message.chat.id, call.message.message_id, reply_markup=main_menu(), parse_mode="Markdown")
    elif call.data in TEXTS:
        markup = InlineKeyboardMarkup()
        markup.add(InlineKeyboardButton("◀️ Главное меню", callback_data="main"))
        bot.edit_message_text(TEXTS[call.data], call.message.chat.id, call.message.message_id, reply_markup=markup, parse_mode="Markdown")
    else:
        bot.answer_callback_query(call.id, "Раздел не найден")

