def handle_callback(call):
    if call.data == "main":
        bot.edit_message_text("📚 **Справочник Arizona RP | Mesa**\n\nВыберите раздел:", 
                              call.message.chat.id, call.message.message_id, 
                              reply_markup=main_menu(), parse_mode="Markdown")
