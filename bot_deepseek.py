def search_rules(query):
    query = query.lower().strip()
    if len(query) < 2:
        return []
    
    cursor.execute('''
    SELECT id, chapter, title, content FROM gov_rules 
    WHERE title LIKE ? OR content LIKE ?
    LIMIT 10
    ''', (f'%{query}%', f'%{query}%'))
    
    return cursor.fetchall()

# ======================= ПРАВИЛА ПО ГЛАВЕ =======================
def get_rules_by_chapter(chapter):
    cursor.execute('''
    SELECT id, title FROM gov_rules WHERE chapter = ? ORDER BY id
    ''', (chapter,))
    return cursor.fetchall()

