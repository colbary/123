def search(message):
    query = message.text.lower()
    
    if "правительств" in query:
        gov_cmd(message)
    elif any(w in query for w in ["раци", "департамент", "d", "канал"]):
        d_cmd(message)
    elif any(w in query for w in ["палатк", "газет"]):
        palatki_cmd(message)
    elif any(w in query for w in ["expel", "выгна", "выдвор"]):
        expel_cmd(message)
    elif "лекц" in query:
        lekcii_cmd(message)
    elif "описан" in query:
        opisanie_cmd(message)
    elif "реклам" in query:
        reklama_cmd(message)
    elif any(w in query for w in ["обязанност", "наказан"]):
        obyazannosti_cmd(message)
    elif "транспорт" in query:
        transport_cmd(message)
    elif "уровень" in query or "опасност" in query or "зеленый" in query or "желтый" in query or "красный" in query:
        levels_cmd(message)
    elif "адвокат" in query:
        if "свободн" in query or "время" in query:
            advokaty_free_cmd(message)
        else:
            advokaty_cmd(message)
    else:
        bot.reply_to(message, "❌ Ничего не найдено. Используйте /start для списка команд.")

