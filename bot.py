# ========== ФУНКЦИЯ ИИ-ПОИСКА ПО СВОИМ ДАННЫМ ==========

def ai_search(query):
    query_lower = query.lower()
    found_sections = []
    
    # Ищем разделы, где есть ключевые слова
    for key, text in ALL_TEXTS.items():
        if any(word in text.lower() for word in query_lower.split()):
            found_sections.append((key, text))
    
    if not found_sections:
        return None
    
    # Собираем контекст из найденных разделов
    context = ""
    for key, text in found_sections[:3]:  # берём до 3 разделов
        # Берём первые 1500 символов из каждого раздела
        context += text[:1500] + "\n\n"
    
    # Простой ИИ: ищем предложения, содержащие ключевые слова
    sentences = []
    for line in context.split('\n'):
        if any(word in line.lower() for word in query_lower.split()):
            sentences.append(line.strip())
    
    if sentences:
        answer = "🤖 **Найдено в правилах:**\n\n" + "\n".join(sentences[:10])
        return answer
    else:
        # Если точных совпадений нет, показываем первые абзацы найденных разделов
        answer = "🤖 **Найдены разделы по вашему вопросу:**\n\n"
        for key, text in found_sections[:3]:
            title = {
                "smi": "СМИ", "gcl": "ГЦЛ", "gov": "Правительство", "d": "Канал /d",
                "palatki": "Палатки", "expel": "/expel", "lekcii": "Лекции",
                "opisanie": "Описание", "reklama": "Реклама", "obyazannosti": "Обязанности",
                "transport": "Транспорт", "advokaty": "Адвокаты"
            }.get(key, key)
            first_lines = text.split('\n')[:5]
            answer += f"📌 **{title}**\n" + "\n".join(first_lines) + "\n\n---\n\n"
        return answer

